num = int(input())
result = ''

if num == 1:
    result = 'Monday'
elif num == 2:
    result = 'Tuesday'
elif num == 3:
    result = 'Wednesday'
elif num == 4:
    result = 'Thursday'
elif num == 5:
    result = 'Friday'
elif num == 6:
    result = 'Saturday'
elif num == 7:
    result = 'Sunday'
else:
    result = 'Error'

print(result)

